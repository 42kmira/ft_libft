#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *head;

	head = s1;
	while (*s1)
		s1++;
	while (n-- > 0)
		if (*s2 != '\0')
		{
			*s1 = *s2;
			s2++;
			s1++;
		}
	*s1 = '\0';
	return (head);
}
